    public void organizarTable() {

        try {
            NMarca oNMarca = new NMarca(arquivoMarca);
            ArrayList<EMarca> listaMarcas;
            listaMarcas = oNMarca.listagem();
            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("Código");
            modelo.addColumn("Descrição");

            if (listaMarcas.isEmpty()) {
                modelo.addRow(new String[]{"Nenhum valor encontrado", "Nenhum valor encontrado"});
            } else {
                listaMarcas.stream().map((marca) -> marca.desmaterializar().split(";")).forEachOrdered((dados) -> {
                    modelo.addRow(dados);
                });
            }
            jTable1.setModel(modelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }

    }

