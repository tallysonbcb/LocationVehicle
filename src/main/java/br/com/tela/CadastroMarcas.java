    private void jButtonIncluirMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirMarcaActionPerformed
        try {
            oEMarca = new EMarca();
            oGeradorID = new GeradorID();
            NMarca oNmarca = new NMarca(arquivoMarca);

            oEMarca.setDescricaoMarca(jTextFieldDescricaoMarca.getText().toUpperCase().trim());
            oEMarca.setIdMarca(oGeradorID.getIdentificadorContato());

            oNmarca.incluir(oEMarca);

            oGeradorID.finalize();

            jTextFieldDescricaoMarca.setText(null);
            JOptionPane.showMessageDialog(rootPane, "Marca incluída com sucesso");
            this.organizarTable();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }

    }//GEN-LAST:event_jButtonIncluirMarcaActionPerformed

    private void jButtonExcluirMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirMarcaActionPerformed
        try {
            String dados = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 1);
            
            NMarca oNMarca = new NMarca(arquivoMarca);
            oNMarca.excluir(dados);
            this.organizarTable();
            this.jButtonExcluirMarca.setEnabled(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }

    }//GEN-LAST:event_jButtonExcluirMarcaActionPerformed

